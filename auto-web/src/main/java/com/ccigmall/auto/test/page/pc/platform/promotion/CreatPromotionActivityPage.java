package com.ccigmall.auto.test.page.pc.platform.promotion;

import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;
import org.openqa.selenium.support.How;

import com.ccigmall.auto.test.page.SettingPage;
/**
 * 
 * @author chenjingli
 *
 */
public class CreatPromotionActivityPage extends SettingPage {
	/**
	 * 创建活动 page
	 * @param driver
	 */
	public CreatPromotionActivityPage(WebDriver driver) {
		super(driver);
	}
	
	
	/**
	 * 促销管理中的，创建活动菜单
	 */
	@FindBy(xpath="//a[contains(text(),'创建活动')]")
	private WebElement XpathCreateActive;
	/**
	 * 输入活动名称
	 */
	@FindBy(id="activeName")
	private WebElement IdTypeActiveName;
	/**
	 * 输入开始时间
	 */
	@FindBy(id="expiringFrom")
	private WebElement IdTypeStartTime;
	/**
	 * 输入结束时间
	 */
	@FindBy(id="expiringTo")
	private WebElement IdTypeEndTime;
	/**
	 * 输入活动描述
	 */
	@FindBy(id="description")
	private WebElement IdTypeDescription;
	/**
	 * 输入广告语
	 */
	@FindBy(id="activeMsg")
	private WebElement IdTypeActiveMsg;
	/**
	 * 点击确定按钮
	 */
	@FindBy(id="confirmButton")
	private WebElement IdClickSubmit;
	/**
	 * 点击取消按钮
	 */
	@FindBy(id="backButton")
	private WebElement IdClickRemoveButton;
	/**
	 * 点击弹出框中的确定
	 */
	@FindBy(xpath="//div[@class='ui_buttons']/input[@value='确定']")
	private WebElement XpathClickConfirm;
	
	/**
	 * 促销管理中的，创建活动菜单
	 */
	public WebElement getXpathCreateActive() {
		return XpathCreateActive;
	}
	/**
	 * 输入活动名称
	 */
	public WebElement getIdTypeActiveName() {
		return IdTypeActiveName;
	}
	/**
	 * 输入开始时间
	 */
	public WebElement getIdTypeStartTime() {
		return IdTypeStartTime;
	}
	/**
	 * 输入结束时间
	 */
	public WebElement getIdTypeEndTime() {
		return IdTypeEndTime;
	}
	/**
	 * 输入活动描述
	 */
	public WebElement getIdTypeDescription() {
		return IdTypeDescription;
	}
	/**
	 * 输入广告语
	 */
	public WebElement getIdTypeActiveMsg() {
		return IdTypeActiveMsg;
	}
	/**
	 * 点击确定按钮
	 */
	public WebElement getIdClickSubmit() {
		return IdClickSubmit;
	}
	/**
	 * 点击取消按钮
	 */
	public WebElement getIdClickRemoveButton() {
		return IdClickRemoveButton;
	}
	/**
	 * 点击弹出框中的确定
	 */
	public WebElement getXpathClickConfirm() {
		return XpathClickConfirm;
	}
	
	
	
	
	
}
