package com.ccigmall.auto.test.page.pc.retailer.my.account;

import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;

import com.ccigmall.auto.test.page.pc.retailer.my.MyIndexPage;
/**
 * 
 * @author chenjingli
 *  我的鑫网-我的订单-账户中心-修改商户信息
 */
public class BusinessInfoPage extends MyIndexPage {

	public BusinessInfoPage(WebDriver driver) {
		super(driver);
	}
	
	/**
	 * 手机
	 */
	@FindBy(id="userPhone")
	public WebElement idTypePhone;
	/**
	 * 邮箱
	 */
	@FindBy(id="userMail")
	public WebElement idTypeEmail;
	/**
	 * fax
	 */
	@FindBy(id="userTel")
	public WebElement idTypeFax;
	/**
	 * 修改按钮
	 */
	@FindBy(id="submit2")
	public WebElement idCliUpdButton;
	public WebElement getIdTypePhone() {
		return idTypePhone;
	}
	public WebElement getIdTypeEmail() {
		return idTypeEmail;
	}
	public WebElement getIdTypeFax() {
		return idTypeFax;
	}
	public WebElement getIdCliUpdButton() {
		return idCliUpdButton;
	}
	
	
}
