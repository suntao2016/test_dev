package com.ccigmall.auto.test.act.pc.category.category;

import org.openqa.selenium.WebDriver;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import com.ccigmall.auto.test.act.pc.category.IndexAct;
import com.ccigmall.auto.test.page.pc.category.category.CategoryManagePage;

/**
 * @author jianping.gao
 * 类目管理
 */
public class CategoryManageAct extends IndexAct{
	
	/**
	 * slf4j
	 */
	private final static Logger logger = LoggerFactory
			.getLogger(CategoryManageAct.class);

	/**
	 * 类目管理
	 */
	private CategoryManagePage categoryManagePage;

	/**
	 * 
	 * @param driver
	 */
	public CategoryManageAct(WebDriver driver) {
		super(driver);
		categoryManagePage=new CategoryManagePage(driver);
	}
	
	/**
	 * 点击产品线管理
	 */
	public void clickProductLineManagement(){
		logger.info("点击产品线管理");
		click(categoryManagePage.getProductLineManagement());
	}
	

	/**
	 * 点击产品线模板管理
	 */
	public void clickProductLineModleManagement(){
		logger.info("点击产品线模板管理");
		click(categoryManagePage.getProductLineModleManagement());
	}
	
	
	
	/**
	 * 点击发布类目管理
	 */
	public void clickPublishedCategoryManagement(){
		logger.info("点击发布类目管理");
		click(categoryManagePage.getPublishedCategoryManagement());
	}
	
	
	/**
	 * 点击类目列表
	 */
	public void clickCategoryList(){
		logger.info("点击类目列表");
		click(categoryManagePage.getCategoryList());
	}
	
	/**
	 * 点击类目树预览
	 */
	public void clickCategoryTressPreview(){
		logger.info("点击类目树预览");
		click(categoryManagePage.getCategoryTressPreview());
	}
	
	/**
	 * 点击展示类目管理
	 */
	public void clickShowCategoryManagement(){
		logger.info("点击展示类目管理");
		click(categoryManagePage.getShowCategoryManagement());
	}
	
	/**
	 * 点击展示类目管理sub
	 */
	public void clickShowCategoryManagementSub(){
		logger.info("点击展示类目管理 sub");
		click(categoryManagePage.getShowCategoryManagementSub());
	}
	
	/**
	 * 点击品牌管理
	 */
	public void clickBrandManagement(){
		logger.info("点击品牌管理");
		click(categoryManagePage.getBrandManagement());
	}
	
	
	/**
	 * 点击品牌列表
	 */
	public void clickBrandList(){
		logger.info("点击品牌列表");
		click(categoryManagePage.getBrandList());
	}
}
