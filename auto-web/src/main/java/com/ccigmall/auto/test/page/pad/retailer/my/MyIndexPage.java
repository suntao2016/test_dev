package com.ccigmall.auto.test.page.pad.retailer.my;

import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;
import org.openqa.selenium.support.How;

import com.ccigmall.auto.test.page.SettingPage;

/**
 * @author jianping.gao
 *  支付
 */
public class MyIndexPage extends SettingPage{

	/**
	 * 
	 * @param driver
	 */
	public MyIndexPage(WebDriver driver) {
		super(driver);
	}
	
	/**
	 * 搜索框
	 */
	@FindBy(how = How.LINK_TEXT, using = "我的订单")
	private WebElement myOrder;

	/**
	 * @return the myOrder
	 */
	public WebElement getMyOrder() {
		return myOrder;
	}
	
	

}
