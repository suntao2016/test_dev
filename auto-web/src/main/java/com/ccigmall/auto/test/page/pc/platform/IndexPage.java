package com.ccigmall.auto.test.page.pc.platform;

import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;
import org.openqa.selenium.support.How;

import com.ccigmall.auto.test.page.SettingPage;

/**
 * @author jianping.gao
 * 供应商首页
 */
public class IndexPage extends SettingPage{

	/**
	 * 
	 * @param driver
	 */
	public IndexPage(WebDriver driver) {
		super(driver);
	}
	
	
	/**
	 * 用户信息
	 *
	 */
	@FindBy(how = How.CLASS_NAME, using = ".m1")
	private WebElement wofe;
	
	/**
	 * 退出
	 *
	 */
	@FindBy(how = How.CLASS_NAME, using = ".m3")
	private WebElement quit;
	
	/**
	 * 首页
	 *
	 */
	@FindBy(how = How.LINK_TEXT, using = "首页")
	private WebElement index;

	/**
	 * 卖家中心
	 * 
	 */
	@FindBy(how = How.LINK_TEXT, using = "卖家中心")
	private WebElement sellerCenter;
	

	/**
	 * 买家中心
	 * 
	 */
	@FindBy(how = How.LINK_TEXT, using = "买家中心")
	private WebElement buyersCenter;
	
	/**
	 * 促销管理
	 * 
	 */
	@FindBy(how = How.LINK_TEXT, using = "促销管理")
	private WebElement promotionManage;
	
	
	/**
	 * 用户中心
	 * 
	 */
	@FindBy(how = How.LINK_TEXT, using = "用户中心")
	private WebElement userCenter;
	
	

	/**
	 * @return the sellerCenter
	 */
	public WebElement getSellerCenter() {
		return sellerCenter;
	}


	/**
	 * @return the userCenter
	 */
	public WebElement getUserCenter() {
		return userCenter;
	}


	/**
	 * @return the index
	 */
	public WebElement getIndex() {
		return index;
	}


	/**
	 * @return the buyersCenter
	 */
	public WebElement getBuyersCenter() {
		return buyersCenter;
	}


	/**
	 * @return the promotionManage
	 */
	public WebElement getPromotionManage() {
		return promotionManage;
	}


	/**
	 * @return the wofe
	 */
	public WebElement getWofe() {
		return wofe;
	}


	/**
	 * @return the quit
	 */
	public WebElement getQuit() {
		return quit;
	}	
	
	
	
	
}
