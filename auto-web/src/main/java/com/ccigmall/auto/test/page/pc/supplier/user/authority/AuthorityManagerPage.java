package com.ccigmall.auto.test.page.pc.supplier.user.authority;

import java.util.List;

import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;
import org.openqa.selenium.support.How;

import com.ccigmall.auto.test.page.pc.supplier.user.UserCenterPage;
/**
 * @author zhaixiaoyue
 * 供应商-用户管理-权限管理
 */
public class AuthorityManagerPage extends UserCenterPage {
	/**
	 * 
	 * @param driver
	 */
	public AuthorityManagerPage(WebDriver driver) {
		super(driver);
	}
	
	/**
	 * 创建新角色按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='addbutton']")
	private WebElement addBtn;
	
	/**
	 * 新角色名
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='name']")
	private WebElement auName;
	
	/**
	 * 修改角色名
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='name1']")
	private WebElement auNameMd;
	
	/**
	 * 添加确定按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='addBtn']")
	private WebElement addSubmmit;
	
	/**
	 * 添加取消按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='addBtnCancel']")
	private WebElement addCancel;
	
	/**
	 * 修改按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='editBtn']")
	private WebElement editBtn;
	
	/**
	 * 修改取消按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='editBtnCancel']")
	private WebElement editBtnCancel;
	
	/**
	 * 删除确认按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//tbody/tr[3]/td/div/input[1]")
	private WebElement delQuery;
	
	/**
	 * 删除取消按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//tbody/tr[3]/td/div/input[2]")
	private WebElement delCancel;
	
	/**
	 * 提示框确认按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//tbody/tr[3]/td/div/input")
	private WebElement query;
	
	/**
	 * 角色列表
	 *
	 */
	@FindBy(how = How.XPATH, using = "//div[@class='au']/li")
	private List<WebElement> tr;
	
	/**
	 * 菜单权限-卖家中心
	 *
	 */
	@FindBy(how = How.XPATH, using = "//*[@id='treeDemo_9_check']")
	private WebElement menuSel;
	
	/**
	 * 菜单权限-保存按钮
	 *
	 */
	@FindBy(how = How.XPATH, using = "//button[@class='b2 f_l']")
	private WebElement SaveBtn;
	
	/**
	 * 删除成功提示框文字
	 *
	 */
	@FindBy(how = How.XPATH, using = "/html/body/div[1]/table/tbody/tr[2]/td[2]/div/table/tbody/tr[2]/td[2]/div")
	private WebElement success;
	
	
	/**
	 * @return the addBtn
	 */
	public WebElement getAddBtn() {
		return addBtn;
	}
	
	
	
	/**
	 * @return the auName
	 */
	public WebElement getAuName() {
		return auName;
	}
	
	/**
	 * @return the auNameMd
	 */
	public WebElement getAuNameMd() {
		return auNameMd;
	}
	
	
	/**
	 * @return the addSubmmit
	 */
	public WebElement getaddSubmmit() {
		return addSubmmit;
	}
	
	/**
	 * @return the addCancel
	 */
	public WebElement getAddCancel() {
		return addCancel;
	}
	
	/**
	 * @return the editBtn
	 */
	public WebElement getEditBtn() {
		return editBtn;
	}
	
	/**
	 * @return the editBtnCancel
	 */
	public WebElement getEditBtnCancel() {
		return editBtnCancel;
	}
	
	/**
	 * @return the delQuery
	 */
	public WebElement getDelQuery() {
		return delQuery;
	}
	
	/**
	 * @return the delCancel
	 */
	public WebElement getDelCancel() {
		return delCancel;
	}
	
	/**
	 * @return the query
	 */
	public WebElement getQuery() {
		return query;
	}
	

	/**
	 * @return the tr
	 */
	public List<WebElement> getTr() {
		return tr;
	}
	
	/**
	 * @return the menuSel
	 */
	public WebElement getMenuSel() {
		return menuSel;
	}
	
	/**
	 * @return the SaveBtn
	 */
	public WebElement getSaveBtn() {
		return SaveBtn;
	}
	
	/**
	 * @return the success
	 */
	public WebElement getSuccess() {
		return success;
	}
	
}
